
#3)Calcular el nuevo salario de un empleado fiscal si obtuvo un reajuste del 4,5% sobre su salario anterior.
#Entrada

SalarioAntiguo = float(input("Ingrese su salario antiguo: "))

#proceso

CambioSalario = SalarioAntiguo * 4.5 / 100

SalarioNuevo = SalarioAntiguo + CambioSalario

#salida

print("El nuevo salario es: ",SalarioNuevo)



