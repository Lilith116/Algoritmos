
#5)El dueño de una tienda compra un artículo a un precio determinado.
# Obtener el precio en que lo debe vender para obtener una ganancia del 32%.

#Entrada

Producto = float(input("Ingresar el precio del producto: "))

#proceso

Ganancia = Producto * 0.32
	
PrecioGanancia = Producto + Ganancia

#salida

print("Para tener una ganancia del 32 porciento, el precio de venta debera ser: ",PrecioGanancia )




