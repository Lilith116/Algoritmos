# Determinar el tiempo promedio que la persona tarda en recorrer la ruta en una semana cualquiera.

#entrada

tiempo1 = float(input("Ingrese su primer tiempo en minutos: "))
tiempo2 = float(input("Ingrese su segundo tiempo en minutos: "))
